#ifndef VECTOR_HPP
#define VECTOR_HPP

//////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////
#define	VECTOR_DEFAULT_CAPACITY 64

typedef unsigned long int count_t;
//////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////
template <typename VT> class Vector
{
protected:
	VT* m_buffer;
	count_t m_counter;
	count_t m_capacity;

public:
	Vector(void)
	{
		m_capacity = VECTOR_DEFAULT_CAPACITY;
		m_counter = 0;
		m_buffer = new VT[m_capacity];
	}
	virtual ~Vector(void)
	{
		delete [] m_buffer;
	}
};
//////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////

#endif